void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == USART2)
	{
		osMailPut(myMail01Handle,data);
		HAL_UART_Receive_IT(&huart2,data,2);
	}
	
	if(huart->Instance == USART3)
	{
		Temp[counter]=data2[0];
		if(counter == 0 && Temp[0] != 0x55)
		{		
			HAL_UART_Receive_IT(&huart3,data2,1);
			return; //第0个数据不是帧头，丢弃   
		}			
    counter++; 
		//printf("%d \n",counter);
    if(counter==11) //接收到11个数据
    { 
			memcpy(Re_buf,Temp,11);   //将Temp数组复制到Re_buf
      counter=0;                //重新赋值，准备下一帧数据的接收
			osMailPut(myMail02Handle,Re_buf);
    }
		HAL_UART_Receive_IT(&huart3,data2,1);
	}
	
	if(huart->Instance == UART4)
	{
		Temp2[counter2]=data3[0];
		if(counter2 == 0 && Temp2[0] != 68) 
		{		
			HAL_UART_Receive_IT(&huart4,data3,1);
			return; //第0个数据不是帧头，丢弃   
		}			
    counter2++; 
    if(counter2==5) //接收到5个数据
    { 
			memcpy(Re_buf2,Temp2,5);   //将Temp数组复制到Re_buf
      counter2=0;                //重新赋值，准备下一帧数据的接收
    }
		HAL_UART_Receive_IT(&huart4,data3,1);
	}	
}

